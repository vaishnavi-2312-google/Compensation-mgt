export interface CompanySummary {
  name: string;
  slug: string;
  logo: string; // text representation or initials
  medianBase: number;
  medianBonus: number;
  medianStock: number;
  medianCompensation: number;
  totalEntries: number;
  levelDistribution: { level: string; count: number }[];
  roleDistribution: { role: string; count: number }[];
  payingLevels: { level: string; medianCompensation: number }[];
}

export function getCompanySummary(companyName: string): CompanySummary | null {
  const entries = mockSalaries.filter(s => s.company.toLowerCase() === companyName.toLowerCase());
  if (entries.length === 0) return null;

  const sortedTC = [...entries].map(e => e.totalCompensation).sort((a, b) => a - b);
  const medianTC = sortedTC[Math.floor(sortedTC.length / 2)];
  
  const sortedBase = [...entries].map(e => e.base).sort((a, b) => a - b);
  const medianBase = sortedBase[Math.floor(sortedBase.length / 2)];

  const sortedBonus = [...entries].map(e => e.bonus).sort((a, b) => a - b);
  const medianBonus = sortedBonus[Math.floor(sortedBonus.length / 2)];

  const sortedStock = [...entries].map(e => e.stock).sort((a, b) => a - b);
  const medianStock = sortedStock[Math.floor(sortedStock.length / 2)];

  // Level Distribution
  const levelCounts: Record<string, number> = {};
  entries.forEach(e => {
    levelCounts[e.level] = (levelCounts[e.level] || 0) + 1;
  });
  const levelDistribution = Object.entries(levelCounts).map(([level, count]) => ({ level, count }));

  // Role Distribution
  const roleCounts: Record<string, number> = {};
  entries.forEach(e => {
    roleCounts[e.role] = (roleCounts[e.role] || 0) + 1;
  });
  const roleDistribution = Object.entries(roleCounts).map(([role, count]) => ({ role, count }));

  // Paying Levels (average or median per level)
  const levelTCs: Record<string, number[]> = {};
  entries.forEach(e => {
    if (!levelTCs[e.level]) levelTCs[e.level] = [];
    levelTCs[e.level].push(e.totalCompensation);
  });
  const payingLevels = Object.entries(levelTCs).map(([level, tcs]) => {
    const sorted = tcs.sort((a, b) => a - b);
    return {
      level,
      medianCompensation: sorted[Math.floor(sorted.length / 2)],
    };
  }).sort((a, b) => b.medianCompensation - a.medianCompensation);

  return {
    name: entries[0].company,
    slug: companyName.toLowerCase(),
    logo: entries[0].company.slice(0, 2).toUpperCase(),
    medianBase,
    medianBonus,
    medianStock,
    medianCompensation: medianTC,
    totalEntries: entries.length,
    levelDistribution,
    roleDistribution,
    payingLevels,
  };
}

